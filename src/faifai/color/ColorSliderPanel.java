    private void jSliderStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSliderStateChanged
      JSlider theSlider = (JSlider) evt.getSource();
      if (theSlider == jSlider1) {
        this.red = theSlider.getValue();
        updateRGBTextField(this.red, jTextField1, jTextField2);
      } else if (theSlider == jSlider2) {
        this.green = theSlider.getValue();
        updateRGBTextField(this.green, jTextField3, jTextField4);
      } else if (theSlider == jSlider3) {
        this.blue = theSlider.getValue();
        updateRGBTextField(this.blue, jTextField5, jTextField6);
      }
      callColorSliderListeners();
    }//GEN-LAST:event_jSliderStateChanged

