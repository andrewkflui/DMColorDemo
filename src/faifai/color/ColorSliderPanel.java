  public int getColor() {
    return ((red << 16) + (green << 8) + blue) & 0x00ffffff;
  }

  public void setSelectedColor(int red, int green, int blue) {
    this.red = red;
    this.green = green;
    this.blue = blue;
  }

  public void setSelectedColor(int rgb) {
    this.red = (rgb >> 16) & 0xff;
    this.green = (rgb >> 8) & 0xff;
    this.blue = rgb & 0xff;
  }

  public void updateDisplay() {
    updateRGBSlider(this.red, jSlider1);
    updateRGBTextField(this.red, jTextField1, jTextField2);
    updateRGBSlider(this.green, jSlider2);
    updateRGBTextField(this.green, jTextField3, jTextField4);
    updateRGBSlider(this.blue, jSlider3);
    updateRGBTextField(this.blue, jTextField5, jTextField6);
    if (jPanel5.isVisible()) {
      float hsl[] = Color.RGBtoHSB(this.red, this.green, this.blue, null);
      updateHSLControls(hsl[0], hsl[1], hsl[2]);
      updateHSLLabels(hsl[0], hsl[1], hsl[2]);
    }
  }

  public void updateHSLSlider() {
    if (jPanel5.isVisible()) {
      float hsl[] = Color.RGBtoHSB(this.red, this.green, this.blue, null);
      updateHSLControls(hsl[0], hsl[1], hsl[2]);
      updateHSLLabels(hsl[0], hsl[1], hsl[2]);
    }
  }

  private void updateRGBSlider(int value, JSlider theSlider) {
    theSlider.setValue(value);
  }

  private void updateRGBTextField(int value, JTextField dec, JTextField hex) {
    dec.setText(value + "");
    if (value < 16) {
      hex.setText("0" + Integer.toHexString(value).toUpperCase());
    } else {
      hex.setText(Integer.toHexString(value).toUpperCase());
    }
  }

  private int getHexValue(String text) {
    try {
      int value = Integer.parseInt(text, 16);
      if (value < 0 || value > 255) {
        return -1;
      }
      return value;
    } catch (Exception ex) {
      return -1;
    }
  }

  private int getDecValue(String text) {
    try {
      int value = Integer.parseInt(text, 10);
      if (value < 0 || value > 255) {
        return -1;
      }
      return value;
    } catch (Exception ex) {
      return -1;
    }
  }

  public void setVisibleColorChooser(boolean isVisible) {
    jPanel4.setVisible(isVisible);
  }

  public void setVisibleHSLChooser(boolean isVisible) {
    jPanel5.setVisible(isVisible);
  }

  public void addColorSliderListener(ColorSliderListener listener) {
    listenerList.add(listener);
  }

  private void callColorSliderListeners() {
    for (ColorSliderListener listener : listenerList) {
      listener.colorChanged(this, red, green, blue);
    }
  }

  private void updateHSLLabels(float hue, float saturation, float lightness) {
    jLabel5.setText("" + (int) (hue * 360 % 360));
    jLabel8.setText("" + saturation);
    jLabel11.setText("" + lightness);
  }

  private void updateHSLControls(float hue, float saturation, float lightness) {
    jSlider4.setValue((int) (hue * 100) % 100);
    jSlider5.setValue((int) saturation * 100);
    jSlider6.setValue((int) lightness * 100);
  }

