    private void hueSliderStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_hueSliderStateChanged
      JSlider theSlider = (JSlider) evt.getSource();
      if (theSlider.getValueIsAdjusting()) {
        return;
      }
      double hue = jSlider4.getValue() / 100.0;
      double sat = jSlider5.getValue() / 100.0;
      double lightness = jSlider6.getValue() / 100.0;
      int rgb = Color.HSBtoRGB((float) hue, (float) sat, (float) lightness);
      setSelectedColor(rgb);

      updateRGBSlider(this.red, jSlider1);
      updateRGBTextField(this.red, jTextField1, jTextField2);
      updateRGBSlider(this.green, jSlider2);
      updateRGBTextField(this.green, jTextField3, jTextField4);
      updateRGBSlider(this.blue, jSlider3);
      updateRGBTextField(this.blue, jTextField5, jTextField6);

      updateHSLLabels((float) hue, (float) sat, (float) lightness);
    }//GEN-LAST:event_hueSliderStateChanged

